function letras(caracter) {
    var alfabeto = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'ñ', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z'];
    var areves = ['z', 'y', 'x', 'w', 'v', 'u', 't', 's', 'r', 'q', 'p', 'o', 'ñ', 'n', 'm', 'l', 'k', 'j', 'i', 'h', 'g', 'f', 'e', 'd', 'c', 'b', 'a'];
    for (i = 0; i < alfabeto.length; i++) {
        if (alfabeto[i] === caracter) {
            return areves[i];
        }
    }
    return caracter;
}

function cesarShift(texto, shift) {
    var resultado = "";
    for (i = 0; i < texto.length; i++) {
        var c = texto.charCodeAt(i);
        if (65 <= c && c <= 90) {
            resultado += String.fromCharCode((c - 65 + shift) % 26 + 65);
        } else if (97 <= c && c <= 122) {
            resultado += String.fromCharCode((c - 97 + shift) % 26 + 97);
        } else {
            resultado += texto.charAt(i);
        }
    }
    return resultado;
}

